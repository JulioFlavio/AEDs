namespace Buraco.Models
{
    // ============================================================================
    // ENUM: Naipe
    // ----------------------------------------------------------------------------
    // OBJETIVO:
    //   Representar os quatro naipes de um baralho comum.
    //   Um "enum" e uma lista fixa de valores com nome, o que torna o codigo
    //   mais legivel do que usar numeros soltos (0,1,2,3) para os naipes.
    // ============================================================================
    public enum Naipe
    {
        Copas,    // simbolo: copas
        Ouros,    // simbolo: ouros
        Paus,     // simbolo: paus
        Espadas   // simbolo: espadas
    }
}
